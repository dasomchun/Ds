package a1229;

public class Hello {
    public static void main(String[] args) {
        System.out.println("Hello , Java!" );
        // java 실행되면 제일먼저 main 메소드가 실행된다.
        // Public : 접근 제어자 -외부에서 접근 할 수 있는 제약
        // Private  -> protected -> default -> public
        // Static : 정적 - 객체 생성 없이도 사용 가능
        // 공통으로 하나만 존재,메모리의 한번만 생성
        // Void : 반환타입 - 반환값이 없다.

    }
}
